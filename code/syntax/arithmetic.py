# Examples of common arithmetic operations in Python
a = 2 + 4 # 6
b = 3.4 - 7.2 # -3.8
c = 5.8 * 3.6 # 20.88
d = -4.5 / 7.3 # -0.616
e = 59 % 4 # 3
f = 3.6 ** 1.3 # 5.29

# Remember that the precedence of an operation can be controlled using brackets
g = 10.0 * (4.7 - 3.2) # 15.0
h = ((1.3 + 7.2) * (9.4 ** 2)) / (9.2 + 1.4) # 70.85